RIM_BACK_CENTER_LOCATION = (8, 0, -4)
RIM_BACK_CENTER_SIZE     = 12.0
RIM_BACK_CENTER_COLOR    = (1.0, 1.0, 1.0)

# -- CAMERA (top-down by default; edit or use extract_camera_params.py) --

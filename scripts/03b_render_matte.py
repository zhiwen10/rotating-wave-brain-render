    scene.render.image_settings.file_format = 'PNG'
    scene.render.image_settings.color_depth = '16'
    scene.render.filepath = RENDER_OUTPUT
    bpy.ops.render.render(write_still=True)
    print(f"Saved: {RENDER_OUTPUT}")
